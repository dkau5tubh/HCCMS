package com.seed.beans;

/*this is pojo class*/

public class Qualification {

	private String ssc;
	private String hsc;
	private String graduation;
	private String postGraduation;
	private String phD;
	private String researchAndDevelopment;

	public String getSsc() {
		return ssc;
	}

	public void setSsc(String ssc) {
		this.ssc = ssc;
	}

	public String getHsc() {
		return hsc;
	}

	public void setHsc(String hsc) {
		this.hsc = hsc;
	}

	public String getGraduation() {
		return graduation;
	}

	public void setGraduation(String graduation) {
		this.graduation = graduation;
	}

	public String getPostGraduation() {
		return postGraduation;
	}

	public void setPostGraduation(String postGraduation) {
		this.postGraduation = postGraduation;
	}

	public String getPhD() {
		return phD;
	}

	public void setPhD(String phD) {
		this.phD = phD;
	}

	public String getResearchAndDevelopment() {
		return researchAndDevelopment;
	}

	public void setResearchAndDevelopment(String researchAndDevelopment) {
		this.researchAndDevelopment = researchAndDevelopment;
	}

}
